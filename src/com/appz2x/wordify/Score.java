package com.appz2x.wordify;

public class Score {
	
	////////////////////////////////////////////////
	// FIELD's & VARIABLES
	////////////////////////////////////////////////
	
	private int id;
	private int score;
	
	////////////////////////////////////////////////
	// CONSTRUCTOR
	////////////////////////////////////////////////
	
	public Score(){}
	
	////////////////////////////////////////////////
	// SETTER'S
	////////////////////////////////////////////////
	
	public void setId(int id){
		this.id = id;
	}
	
	public void setScore(int score){
		this.score = score;
	}
	
	////////////////////////////////////////////////
	// GETTER'S
	////////////////////////////////////////////////
	
	public int getId(){
		return this.id;
	}
	
	public int getScore(){
		return this.score;
	}
}
